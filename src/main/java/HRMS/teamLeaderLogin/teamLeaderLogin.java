package HRMS.teamLeaderLogin;

public class teamLeaderLogin {

	private int teamId;
	private String teamUsername;
	private String teamPassword;
	private int empId;
	private String appraisalCode;
	private int performanceRating;

	public int getTeamId() {
		return teamId;
	}
	public void setTeamId(int teamId) {
		this.teamId = teamId;
	}
	public String getTeamUsername() {
		return teamUsername;
	}
	public void setTeamUsername(String teamUsername) {
		this.teamUsername = teamUsername;
	}
	public String getTeamPassword() {
		return teamPassword;
	}
	public void setTeamPassword(String teamPassword) {
		this.teamPassword = teamPassword;
	}
	public int getEmpId() {
		return empId;
	}
	public void setEmpId(int empId) {
		this.empId = empId;
	}
	public String getAppraisalCode() {
		return appraisalCode;
	}
	public void setAppraisalCode(String appraisalCode) {
		this.appraisalCode = appraisalCode;
	}
	public int getPerformanceRating() {
		return performanceRating;
	}
	public void setPerformanceRating(int performanceRating) {
		this.performanceRating = performanceRating;
	}
	

}
